export type ThemeKey = keyof typeof themes

export function applyTheme(theme: ThemeKey, mode: 'light' | 'dark') {
  const root = document.documentElement
  const themeVars = themes[theme].cssVars[mode]
  
  Object.entries(themeVars).forEach(([key, value]) => {
    root.style.setProperty(`--${key}`, value)
  })
  
  // Store in localStorage
  localStorage.setItem('bbpad-theme', theme)
  localStorage.setItem('bbpad-theme-mode', mode)
}

export function getStoredTheme(): { theme: ThemeKey, mode: 'light' | 'dark' } {
  const theme = (localStorage.getItem('bbpad-theme') as ThemeKey) || 'default'
  const mode = (localStorage.getItem('bbpad-theme-mode') as 'light' | 'dark') || 'light'
  return { theme, mode }
}

export function initializeTheme() {
  const { theme, mode } = getStoredTheme()
  applyTheme(theme, mode)
}